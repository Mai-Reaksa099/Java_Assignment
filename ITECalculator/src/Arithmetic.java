/**
 *Class Arithmetic use for calculator for Math operator
 */
public class Arithmetic {

    /**
     * Method sum use for adding number kind of integer
     * @param num1 is parameter must integer
     * @param num2 is parameter must integer
     * @return give value after adding
     */
    public int sum(int num1, int  num2){

        return num1 + num2;
    }

    /**
     *
     * @param num1
     * @param num2
     * @return
     */
    public double divide(int num1, int num2){

        return  num1 /  num2;
    }

    /**
     *
     * @param num1
     * @param num2
     * @return
     */
    public int minus(int num1, int num2){

        return num1 - num2;
    }

    /**
     *
     * @param num1
     * @param num2
     * @return
     */
    public int multiply(int num1, int num2){

        return  num1 * num2;
    }

    /**
     *
     * @param num1
     * @param num2
     * @return
     */
    public int modulo(int num1, int num2){

        return  num1 % num2;
    }
}
